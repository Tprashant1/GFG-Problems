class Solution {
  public:
    // a,b : the arrays
    // Function to return a list containing the union of the two arrays.
    vector<int> findUnion(vector<int> &a, vector<int> &b) {
        set<int>st;
        vector<int>temp;
        for(auto i:a){
            st.insert(i);
            temp.push_back(i);
        }
        
        for(auto i:b){
            st.insert(i);
            temp.push_back(i);
        }
        
        vector<int>ans;
        for(auto i:temp){
            if(st.find(i) != st.end()){
                ans.push_back(i);
                st.erase(i);
            }
        }
        
        sort(ans.begin(),ans.end());
        return ans;
    }
};

