class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        sort(arr.rbegin(),arr.rend());
        int n = arr.size();
        set<int>st;
        for(int i=0;i<n;i++){
            int element = arr[i];
            st.insert(element);
            if(st.size() == 2){
                return element;
            }
        }
        return -1;
    }
};

