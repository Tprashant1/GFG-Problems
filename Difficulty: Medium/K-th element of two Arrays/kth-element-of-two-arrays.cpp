class Solution {
  public:
    int kthElement(vector<int>& a, vector<int>& b, int k) {
        vector<int>temp;
        for(auto i:a){
            temp.push_back(i);
        }
        
        for(auto i:b){
            temp.push_back(i);
        }
        
        sort(temp.begin(),temp.end());
        int n = temp.size();
        for(int i=0;i<n;i++){
            if(i == k-1){
                return temp[i];
            }
        }
        return 0;
    }
};

